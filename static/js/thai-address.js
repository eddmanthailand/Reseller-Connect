function getProvinces() {
    return Object.keys(THAI_ADDRESSES).sort();
}

function getDistricts(province) {
    if (!province || !THAI_ADDRESSES[province]) return [];
    return Object.keys(THAI_ADDRESSES[province]).sort();
}

function getSubdistricts(province, district) {
    if (!province || !district || !THAI_ADDRESSES[province] || !THAI_ADDRESSES[province][district]) return [];
    return Object.keys(THAI_ADDRESSES[province][district]).sort();
}

function getPostalCode(province, district, subdistrict) {
    if (!province || !district || !subdistrict) return '';
    try {
        return THAI_ADDRESSES[province][district][subdistrict] || '';
    } catch (e) {
        return '';
    }
}

function populateProvinceSelect(selectId, onChange) {
    const select = document.getElementById(selectId);
    if (!select) return;
    
    select.innerHTML = '<option value="">-- เลือกจังหวัด --</option>';
    getProvinces().forEach(province => {
        const option = document.createElement('option');
        option.value = province;
        option.textContent = province;
        select.appendChild(option);
    });
    
    if (onChange) {
        select.addEventListener('change', onChange);
    }
}

function populateDistrictSelect(selectId, province, onChange) {
    const select = document.getElementById(selectId);
    if (!select) return;
    
    select.innerHTML = '<option value="">-- เลือกเขต/อำเภอ --</option>';
    getDistricts(province).forEach(district => {
        const option = document.createElement('option');
        option.value = district;
        option.textContent = district;
        select.appendChild(option);
    });
    
    if (onChange) {
        select.removeEventListener('change', onChange);
        select.addEventListener('change', onChange);
    }
}

function populateSubdistrictSelect(selectId, province, district, onChange) {
    const select = document.getElementById(selectId);
    if (!select) return;
    
    select.innerHTML = '<option value="">-- เลือกแขวง/ตำบล --</option>';
    getSubdistricts(province, district).forEach(subdistrict => {
        const option = document.createElement('option');
        option.value = subdistrict;
        option.textContent = subdistrict;
        select.appendChild(option);
    });
    
    if (onChange) {
        select.removeEventListener('change', onChange);
        select.addEventListener('change', onChange);
    }
}

function initWarehouseAddressCascade() {
    populateProvinceSelect('warehouseProvince', function() {
        const province = this.value;
        populateDistrictSelect('warehouseDistrict', province, function() {
            const district = this.value;
            populateSubdistrictSelect('warehouseSubdistrict', province, district, function() {
                const subdistrict = this.value;
                const postalCode = getPostalCode(province, district, subdistrict);
                document.getElementById('warehousePostalCode').value = postalCode;
            });
            document.getElementById('warehouseSubdistrict').value = '';
            document.getElementById('warehousePostalCode').value = '';
        });
        document.getElementById('warehouseDistrict').value = '';
        document.getElementById('warehouseSubdistrict').value = '';
        document.getElementById('warehousePostalCode').value = '';
    });
}
